package workouts;

public class StringExample {
    public static void main(String args[]) {
        String s1 = "Hello World";
        char ch[] = {'P', 'r', 'i', 'y', 'a'};
        String s2 = new String(ch);
        String s3 = new String("Welcome");
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
